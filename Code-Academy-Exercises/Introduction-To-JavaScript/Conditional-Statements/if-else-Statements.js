let sale = true;

sale = false;

if(sale) {
  console.log('Time to buy!');
} else {
  console.log('Time to wait for a sale.');
}



/* An else statement can be used in conjunction with an if statement to execute an action when the if statement evaluates false. The else statement on line 7 will execute when the "sale" variable is false. Because "sale" was reassigned the value of "false", the string "Time to wait for a sale" printed in the console. */