let myName = 'Sam';
let myCity = 'Louisville, CO';
console.log(`My name is ${myName}. My favorite city is ${myCity}`);



/*Variables can be inserted into a string using template literals. On line 3 above the string wihtin the "console.log" method is contained inside a pair of backticks, and the variables "myName" and "myCity" are inserted into the string using place holders(${}). As a result the following is printed onto the console: "My name is Sam. My favorite city is Louisville, CO". Template literals makes code easier to read, as you can see whatna new string will be before running the code. */